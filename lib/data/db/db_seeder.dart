/// Seeds the local database with initial reference data.
///
/// This class inserts predefined meal plans, vitamins, foods,
/// and vitamin-to-food links into the database using idempotent
/// operations so that repeated seeding does not create duplicates.
class DbSeeder {
  /// Creates a private singleton instance of the database seeder.
  DbSeeder._();

  /// Shared singleton instance used to seed the database.
  static final DbSeeder instance = DbSeeder._();

  /// Populates the database with initial reference data.
  ///
  /// The method inserts predefined meal plans, vitamins, foods,
  /// and vitamin-to-food relations inside a single transaction.
  ///
  /// Repeated execution is safe because inserts use conflict handling
  /// to avoid creating duplicate records.
  Future<void> seed() async {
    final db = await AppDb.instance.db;

    await db.transaction((txn) async {
      for(final desc in _seedMealPlans){
        await _ensureMealPlan(txn, desc);
      }

      for(final vName in _seedVitamins){
        await _getOrCreateIdByName(txn, table: 'vitamins', name: vName);
      }

      for(final food in _seedFood){
        await _getOrCreateFoodId(txn, food);
      }

      for (final link in _seedVitFoodLinks) {
        final foodId = await _getIdByName(txn, table: 'food',
            name: link.foodName);
        final vitaminId = await _getIdByName(txn, table: 'vitamins',
            name: link.vitaminName);

        if (foodId == null || vitaminId == null) {
          continue;
        }

        await txn.insert(
          'vit_food',
          {
            'food_id': foodId,
            'vitamin_id': vitaminId,
            'amount_per_100g': link.amountPer100g,
          },
          conflictAlgorithm: ConflictAlgorithm.ignore,
        );
      }
    });
  }


  Future<void> _ensureMealPlan(DatabaseExecutor db, String description) async {
    final rows = await db.query(
      'meal_plans',
      columns: ['id'],
      where: 'description = ?',
      whereArgs: [description],
      limit: 1,
    );

    if (rows.isNotEmpty) return;

    await db.insert('meal_plans', {'description': description});
  }

  Future<int> _getOrCreateIdByName(DatabaseExecutor db, {
    required String table,
    required String name,
  }) async {
    await db.insert(
      table,
      {'name': name},
      conflictAlgorithm: ConflictAlgorithm.ignore,
    );

    final id = await _getIdByName(db, table: table, name: name);
    if (id == null) {
      throw StateError('Cannot get id for $table name="$name"');
    }
    return id;
  }

  Future<int> _getOrCreateFoodId(DatabaseExecutor db,
      Map<String, Object?> food) async {
    await db.insert(
      'food',
      food,
      conflictAlgorithm: ConflictAlgorithm.ignore,
    );

    final name = food['name'] as String;
    final id = await _getIdByName(db, table: 'food', name: name);
    if (id == null) {
      throw StateError('Cannot get id for food name="$name"');
    }
    return id;
  }

  Future<int?> _getIdByName(DatabaseExecutor db, {
    required String table,
    required String name,
  }) async {
    final rows = await db.query(
      table,
      columns: ['id'],
      where: 'name = ?',
      whereArgs: [name],
      limit: 1,
    );

    if (rows.isEmpty) return null;
    return (rows.first['id'] as num).toInt();
  }
}

